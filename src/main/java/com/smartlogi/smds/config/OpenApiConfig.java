package com.smartlogi.smds.config;

import io.swagger.v3.oas.models.OpenAPI;
import io.swagger.v3.oas.models.info.Info;
import io.swagger.v3.oas.models.info.License;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

@Configuration
public class OpenApiConfig {

    @Bean
    public OpenAPI customOpenAPI() {
        return new OpenAPI()
                .info(new Info()
                        .title("SMDS API - Smartlogi Maroc Delivery System")
                        .version("1.0")
                        .description("API pour la gestion des colis, des livraisons et des ressources associ√©es.")
                        .termsOfService("http://smartlogi.ma/terms/")
                        .license(new License().name("Apache 2.0").url("http://springdoc.org")));
    }
}
